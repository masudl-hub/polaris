import { motion } from 'framer-motion'
import { marked } from 'marked'
import { FileText } from 'lucide-react'
import { fadeUp } from '../../lib/motion'
import EmptyState from '../ui/EmptyState'
import CardInsight from '../ui/CardInsight'

export default function DiagnosticSection({ diagnostic }) {
  if (!diagnostic) {
    return (
      <motion.div
        initial={{ opacity: 0, y: 24 }}
        whileInView={{ opacity: 1, y: 0 }}
        viewport={{ once: true }}
        transition={{ duration: 0.4 }}
      >
        <EmptyState icon={FileText} message="No diagnostic generated" />
      </motion.div>
    )
  }

  const html = marked.parse(diagnostic)

  return (
    <motion.div
      variants={fadeUp}
      initial="hidden"
      whileInView="visible"
      viewport={{ once: true, margin: '-40px' }}
    >
      <CardInsight
        meaning="A comprehensive executive summary synthesizing all analysis results into actionable narrative. This is your 'what to do next' brief."
        significance="While individual cards show data points, the diagnostic connects the dots -- explaining how sentiment, trends, creative quality, and market position interact to predict your ad's performance."
        calculation="Generated by Claude (Sonnet) with the full analysis context: all scores, trends, entities, benchmarks, and competitor data are fed as structured input. The model synthesizes these into a coherent strategic assessment with specific, actionable recommendations."
      >
        <div className="bg-[#252527] rounded-[1.5rem] p-8 shadow-lg">
          <div
            className="diagnostic-prose"
            dangerouslySetInnerHTML={{ __html: html }}
          />
        </div>
      </CardInsight>
    </motion.div>
  )
}
